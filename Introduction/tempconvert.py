print("Welcome to the Temperature Converter (Farenheit to Celsius)")
print()
temp = int(input("Enter the Temparature in Farenheit : ")) # Input

Celsius = ((temp-32)/180)*100                            # Function

print("The Temparature in Celsius is", Celsius)          # Answer

